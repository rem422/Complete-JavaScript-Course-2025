// Operation Precedence
// * / are done first.
// + - are done after.
// We can use () brackets to control which part gets done first.
/* 
Order of Operation
1. (...)
2. * /
3. + -
*/

console.log( 1 + 1 * 3); // 1 * 3 is done first then the 3 + 1 is done after which is equal to 4.
console.log( (1 + 1) * 3); // 1 + 1 is done first then the 2 * 3 which is equal to 6 is done after.

console.log(20.95 + 7.99 * 2);
console.log(36.93 * 0.1);

console.log((20.95 + 7.99 * 2) * 0.1);


